/*
01 비주얼 스튜디오 6.0 사용 방법
비주얼 스튜디오 6.0 설치하기

프로젝트 생성하기
프로젝트 파일의 확장자는 dsp이다.

프로그램 실행하기
소스파일을 빌드해서 실행파일로 만드는 작업을 선행해야 한다.
'Build->Build .exe(F7)'메뉴를 누르면 빌드한 후에 결과 창에 빌드 결과를 표시한다.
'Build->Execute .exe(F5)'를 누르면 프로그램을 실행한다.

워크스페이스
닷넷의 솔루션과 6.0의 워크스페이스는 같은 개념.
워크스페이스의 확장자는 dsw.

경고 레벨 높이기
'Project->Settings(Alt+F7)'메뉴를 누르면 속성이 나오고 C/C++탭에 Warning level을 변경하면 된다.

디버깅 하기
Debug 메뉴는 디버깅 동안에만 보인다.
디버깅 시작하기 : Build->Start Debug->Go(F5)
한 줄씩 실행하기 : Debug->Step Into(F11) or ->Step Over(F10)
디버깅 끝내기 : Debug->Stop Debugging(Shift + F5)

*/